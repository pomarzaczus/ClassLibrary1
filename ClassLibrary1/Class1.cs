using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace kalulator
{
    public class calculator
    {
        public float dod(float a, float b)
        {

            return a+b;
        }
        public float odj(float a, float b)
        {

            return a - b;
        }
        public float dzie(float a, float b)
        {

            return a / b;
        }
        public float mno(float a, float b)
        {

            return a * b;
        }
    }
}
